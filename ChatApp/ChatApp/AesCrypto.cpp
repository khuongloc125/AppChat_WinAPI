static unsigned int ascii2hex(unsigned char c) {
    if (c >= '0' && c <= '9') return c - '0';
    if (c >= 'a' && c <= 'f') return 10 + c - 'a';
    if (c >= 'A' && c <= 'F') return 10 + c - 'A';
    return 0;
}

unsigned char AesCrypto::key[16] = { 0 };
unsigned char AesCrypto::extKey[176] = { 0 };
bool AesCrypto::keyLoaded = false;

bool AesCrypto::LoadKey(const std::string& keyHex32) {
    if (keyLoaded) return true; 
    if (keyHex32.length() != 32) return false;

    for (int i = 0; i < 16; ++i) {
        key[i] = (ascii2hex(keyHex32[i * 2]) << 4) | ascii2hex(keyHex32[i * 2 + 1]);
    }
    extendKey();
    keyLoaded = true;
    return true;
}


static unsigned char mul2(unsigned char x) {
    return (x << 1) ^ ((x & 0x80) ? 0x1b : 0x00);
}

static unsigned char mul3(unsigned char x) {
    return mul2(x) ^ x;
}

static unsigned char mul9(unsigned char x) {
    return mul2(mul2(mul2(x))) ^ x;
}

static unsigned char mul11(unsigned char x) {
    return mul2(mul2(mul2(x))) ^ mul2(x) ^ x;
}

static unsigned char mul13(unsigned char x) {
    return mul2(mul2(mul2(x))) ^ mul2(mul2(x)) ^ x;
}

static unsigned char mul14(unsigned char x) {
    return mul2(mul2(mul2(x))) ^ mul2(mul2(x)) ^ mul2(x);
}

// ================== MỞ RỘNG KEY ==================
void AesCrypto::extendKey() {
    for (int i = 0; i < 16; ++i) extKey[i] = key[i];
    for (int i = 1; i <= 10; ++i) {
        int w = i * 4;
        unsigned char temp[4];
        temp[0] = SBOX[extKey[(w - 1) * 4 + 1]] ^ rcon[i];
        temp[1] = SBOX[extKey[(w - 1) * 4 + 2]];
        temp[2] = SBOX[extKey[(w - 1) * 4 + 3]];
        temp[3] = SBOX[extKey[(w - 1) * 4 + 0]];
        for (int j = 0; j < 4; ++j)
            extKey[w * 4 + j] = extKey[(w - 4) * 4 + j] ^ temp[j];
        for (int k = 1; k < 4; ++k) {
            w++;
            for (int j = 0; j < 4; ++j)
                extKey[w * 4 + j] = extKey[(w - 4) * 4 + j] ^ extKey[(w - 1) * 4 + j];
        }
    }
}

// ================== CÁC HÀM AES ==================
static void addRoundKey(unsigned char* a, const unsigned char* k) {
    for (int i = 0; i < 16; ++i) a[i] ^= k[i];
}

static void subRoundKey(unsigned char* a, const unsigned char* k) {
    addRoundKey(a, k);
}

static void byteSubstituition(unsigned char* a) {
    for (int i = 0; i < 16; ++i) a[i] = SBOX[a[i]];
}

static void invByteSubstituition(unsigned char* a) {
    for (int i = 0; i < 16; ++i) a[i] = INV_SBOX[a[i]];
}

static void shiftRows(unsigned char* a) {
    unsigned char t[16];
    t[0] = a[0];  t[1] = a[5];  t[2] = a[10]; t[3] = a[15];
    t[4] = a[4];  t[5] = a[9];  t[6] = a[14]; t[7] = a[3];
    t[8] = a[8];  t[9] = a[13]; t[10] = a[2];  t[11] = a[7];
    t[12] = a[12]; t[13] = a[1];  t[14] = a[6];  t[15] = a[11];
    for (int i = 0; i < 16; ++i) a[i] = t[i];
}

static void invShiftRows(unsigned char* a) {
    unsigned char t[16];
    t[0] = a[0];  t[1] = a[13]; t[2] = a[10]; t[3] = a[7];
    t[4] = a[4];  t[5] = a[1];  t[6] = a[14]; t[7] = a[11];
    t[8] = a[8];  t[9] = a[5];  t[10] = a[2];  t[11] = a[15];
    t[12] = a[12]; t[13] = a[9];  t[14] = a[6];  t[15] = a[3];
    for (int i = 0; i < 16; ++i) a[i] = t[i];
}

static void mixColumn(unsigned char* a) {
    unsigned char t[16];
    for (int i = 0; i < 4; ++i) {
        t[4 * i + 0] = mul2(a[4 * i + 0]) ^ mul3(a[4 * i + 1]) ^ a[4 * i + 2] ^ a[4 * i + 3];
        t[4 * i + 1] = a[4 * i + 0] ^ mul2(a[4 * i + 1]) ^ mul3(a[4 * i + 2]) ^ a[4 * i + 3];
        t[4 * i + 2] = a[4 * i + 0] ^ a[4 * i + 1] ^ mul2(a[4 * i + 2]) ^ mul3(a[4 * i + 3]);
        t[4 * i + 3] = mul3(a[4 * i + 0]) ^ a[4 * i + 1] ^ a[4 * i + 2] ^ mul2(a[4 * i + 3]);
    }
    for (int i = 0; i < 16; ++i) a[i] = t[i];
}

static void invMixColumn(unsigned char* a) {
    unsigned char t[16];
    for (int i = 0; i < 4; ++i) {
        t[4 * i + 0] = mul14(a[4 * i + 0]) ^ mul11(a[4 * i + 1]) ^ mul13(a[4 * i + 2]) ^ mul9(a[4 * i + 3]);
        t[4 * i + 1] = mul9(a[4 * i + 0]) ^ mul14(a[4 * i + 1]) ^ mul11(a[4 * i + 2]) ^ mul13(a[4 * i + 3]);
        t[4 * i + 2] = mul13(a[4 * i + 0]) ^ mul9(a[4 * i + 1]) ^ mul14(a[4 * i + 2]) ^ mul11(a[4 * i + 3]);
        t[4 * i + 3] = mul11(a[4 * i + 0]) ^ mul13(a[4 * i + 1]) ^ mul9(a[4 * i + 2]) ^ mul14(a[4 * i + 3]);
    }
    for (int i = 0; i < 16; ++i) a[i] = t[i];
}

// ================== ENCRYPT / DECRYPT BLOCK ==================
std::string AesCrypto::Encrypt(const std::string& data) {

    size_t padLen = 16 - (data.size() % 16);
    std::string padded = data;
    for (size_t i = 0; i < padLen; ++i)
        padded += static_cast<char>(padLen);

    std::string ciphertext;
    unsigned char prev[16] = { 0 };

    for (size_t i = 0; i < padded.size(); i += 16) {
        unsigned char block[16];
        memcpy(block, padded.data() + i, 16);

        for (int j = 0; j < 16; ++j) block[j] ^= prev[j];

        addRoundKey(block, extKey);
        for (int r = 1; r < 10; ++r) {
            byteSubstituition(block);
            shiftRows(block);
            mixColumn(block);
            addRoundKey(block, extKey + 16 * r);
        }
        byteSubstituition(block);
        shiftRows(block);
        addRoundKey(block, extKey + 160);

        ciphertext.append(reinterpret_cast<char*>(block), 16);
        memcpy(prev, block, 16);
    }

    std::ostringstream ss;
    for (unsigned char c : ciphertext)
        ss << std::hex << std::setw(2) << std::setfill('0') << (int)c;
    return ss.str();
}

std::string AesCrypto::Decrypt(const std::string& hex) {
    size_t len = hex.length() / 2;
    std::string ciphertext;
    ciphertext.reserve(len);
    for (size_t i = 0; i < len; ++i)
        ciphertext += (ascii2hex(hex[i * 2]) << 4) | ascii2hex(hex[i * 2 + 1]);

    std::string plaintext;
    unsigned char prev[16] = { 0 }; 

    for (size_t i = 0; i < ciphertext.size(); i += 16) {
        unsigned char block[16], temp[16];
        memcpy(block, ciphertext.data() + i, 16);
        memcpy(temp, block, 16);

        addRoundKey(block, extKey + 160);
        invShiftRows(block);
        invByteSubstituition(block);
        for (int r = 9; r > 0; --r) {
            addRoundKey(block, extKey + 16 * r);
            invMixColumn(block);
            invShiftRows(block);
            invByteSubstituition(block);
        }
        addRoundKey(block, extKey);

        for (int j = 0; j < 16; ++j) block[j] ^= prev[j];

        plaintext.append(reinterpret_cast<char*>(block), 16);
        memcpy(prev, temp, 16); 
    }

    if (plaintext.empty()) return "";
    unsigned char pad = plaintext.back();
    if (pad >= 1 && pad <= 16) {
        size_t validLen = plaintext.size() - pad;
        bool valid = true;
        for (size_t i = validLen; i < plaintext.size(); ++i) {
            if (plaintext[i] != pad) { valid = false; break; }
        }
        if (valid) plaintext.resize(validLen);
    }

    return plaintext;
}